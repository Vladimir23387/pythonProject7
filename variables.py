# Тип строка
name = 'Александр'

# Тип целое число
age = 12
school_class = 5

# Тип число с плавающей точкой
average_rating = 4.7

# Логический (булев) тип (True, False)
excellent_student = False

print(type(name))
print(type(age))
print(type(school_class))
print(type(average_rating))
print(type(excellent_student))

